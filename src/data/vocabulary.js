export const VOCABULARY = CATEGORIES.flatMap((cat, ci) =>
  cat.words.map(([word, meaningBn, example], wi) => ({
    id: `v${ci * 20 + wi + 1}`,
    word,
    meaningBn,
    example,
    category: cat.name,
    difficulty: cat.difficulty,
  }))
)

export function getDailyWords(dayIndex, count = 5) {
  const total = VOCABULARY.length
  const start = (dayIndex * count) % total
  const out = []
  for (let i = 0; i < count; i++) {
    out.push(VOCABULARY[(start + i) % total])
  }
  return out
}

export function getWordById(id) {
  return VOCABULARY.find((w) => w.id === id)
}
